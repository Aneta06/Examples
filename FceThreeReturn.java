public class FceThreeReturn {

    public static void main(String[] args) {

        int result = giveMeTen();                     // 3 . fce, slovo result si vymyslim vytiskne 10

        System.out.println(result);
    }

    // value-returning, parameterless
    public static int giveMeTen() {                 // 3. fce giveMeTen je nazev fce, kterej si vymyslim

        return 10;                                  // vrati se mi 10, kdyz to nahore vytisknu
    }
}
